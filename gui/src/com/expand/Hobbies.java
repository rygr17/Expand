package com.expand;
public class Hobbies {

	boolean fishing;
	boolean hiking;
	boolean concerts;
	boolean traveling;
	boolean food;
	boolean baseball;
	boolean football;
	
	public Hobbies(boolean fishing, boolean hiking, boolean concerts, 
			boolean traveling, boolean food, boolean baseball, boolean football){
			this.fishing = fishing;
			this.hiking = hiking;
			this.concerts = concerts;
			this.traveling = traveling;
			this.food = food;
			this.baseball = baseball;
			this.football = football;
	}
	
	
}
